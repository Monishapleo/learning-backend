package com.practice.dsa.arrays;

public class TwoPointers {
    /**
     * ==========================================================
     * Pattern : Two Pointers
     * ==========================================================
     *
     * Description:
     * Two pointers move toward each other or
     * in the same direction to solve problems efficiently.
     *
     * Usually Requires:
     * Sorted Array
     *
     * When to Use:
     * - Pair Sum
     * - Sorted Arrays
     * - Reverse Array
     * - Palindrome
     * - Merge Arrays
     *
     * Mental Model:
     *
     * sum < target
     *      ↓
     * left++
     *
     * sum > target
     *      ↓
     * right--
     *
     * sum == target
     *      ↓
     * Answer Found
     *
     * Why Sorted?
     *
     * left++ increases (or keeps) the value.
     *
     * right-- decreases (or keeps) the value.
     *
     * Common Problems:
     * - Two Sum II
     * - Valid Palindrome
     * - Merge Sorted Arrays
     * - Container With Most Water
     *
     * Time Complexity:
     * O(n)
     *
     * Space Complexity:
     * O(1)
     *
     * ==========================================================
     */

}
